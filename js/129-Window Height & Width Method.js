function resizeFunction() {
    console.clear();

    // innerHeight

    let iHeight = window.innerHeight;
    console.log("Inner Height:" + iHeight);

    // outerHeight

    let oHeight = window.outerHeight;
    console.log("Outer Height: " + oHeight);
}

