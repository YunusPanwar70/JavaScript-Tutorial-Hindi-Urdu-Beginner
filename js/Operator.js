// Equal to 
let a = 10;
let b = 10;

console.log(a == b);

let q = 10;
let r = "10";
console.log(q == r);

// Equal Value And Equal type 
let c = 20;
let d = 10;
console.log(c === d);

// Not Equal 
let e = 30;
let f = 10;
console.log(e != f);

// Not Equal Value Or Not Equal Type 
let g = 10;
let h = 10;
console.log(g ==! h);

// Greater Than 
let i = 20;
let j = 10;
console.log(i > j);

// Less Than 
let k = 10;
let l = 20;
console.log(k < l);

// Greater Than Or Equal to 
let m = 10;
let n = 10;
console.log(m >= n);

// Less Than Or Equal to 
let o = 10;
let p = 10;
console.log(o <= p);